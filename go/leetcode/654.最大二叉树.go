/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

func constructMaximumBinaryTree(nums []int) *TreeNode {
	if len(nums) <= 0 {
		return nil
	}
	idx := 0
	max := nums[idx]
	for i, v := range nums {
		if max < v {
			max = v
			idx = i
		}
	}

	return &TreeNode{
		Val: max,
		Left: constructMaximumBinaryTree(nums[0:idx]),
		Right: constructMaximumBinaryTree(nums[idx+1: len(nums)]),
	}
}
